def get_language_name(lang=None):
    if lang is None:
        lang = _current_lang
    return LANGUAGES.get(lang, "简体中文")


def get_text(category, key=None):
    lang = _current_lang if _current_lang in MEITOU_LINES else "zh_CN"

    if category == "meitou":
        import random
        lines = MEITOU_LINES.get(lang, MEITOU_LINES["zh_CN"])
        return random.choice(lines)
    elif category == "weather_bubble":
        d = WEATHER_BUBBLE.get(lang, WEATHER_BUBBLE["zh_CN"])
        return d.get(key, key)
    elif category == "weather_same":
        import random
        d = WEATHER_SAME.get(lang, WEATHER_SAME["zh_CN"])
        lines = d.get(key, [key])
        return random.choice(lines)
    elif category == "settings":
        d = SETTINGS_LABELS.get(lang, SETTINGS_LABELS["zh_CN"])
        return d.get(key, key)
    elif category == "ui":
        d = UI_TEXT.get(lang, UI_TEXT["zh_CN"])
        result = d.get(key)
        if result is None and lang != "zh_CN":
            result = UI_TEXT["zh_CN"].get(key, key)
        return result if result is not None else key
    return key


def t(key, default=""):
    return get_text("ui", key) or default or key


